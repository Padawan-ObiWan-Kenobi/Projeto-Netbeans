    private void jComboBox1PopupMenuWillBecomeInvisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_jComboBox1PopupMenuWillBecomeInvisible
        String tmp = (String)jComboBox1.getSelectedItem();
        String sql = "select * from produtos where descricao_produto=?";
        
        try{
            
            PreparedStatement pst = conecta.Con.prepareStatement(sql);
            pst.setString(1, tmp);
            conecta.rs=pst.executeQuery();
            if(conecta.rs.next()) {
            String add1 = conecta.rs.getString("id_produto");
            jTextField1.setText(add1);
            String add2 = conecta.rs.getString("descricao_produto");
            jTextField2.setText(add2);
            String add3 = conecta.rs.getString("categoria_produto");
            jTextField3.setText(add3);
            String add4 = conecta.rs.getString("unidade_produto");
            jTextField4.setText(add4);
            }
        }catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Erro ao excluir usu√°rio!/nErro:"+ex);
        }
    }//GEN-LAST:event_jComboBox1PopupMenuWillBecomeInvisible

